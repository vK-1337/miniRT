#include "../includes/libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*p;

	if (nmemb * size == 0)
	{
		p = malloc(0);
		if (!p)
			return (NULL);
		return (p);
	}
	if (!nmemb || !size || ((nmemb * size) / size != nmemb))
		return (NULL);
	p = malloc(nmemb * size);
	if (!p)
		return (NULL);
	ft_bzero(p, nmemb * size);
	return (p);
}
